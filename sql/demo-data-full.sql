    WHEN 1112 THEN 1019  -- 京东健康
  END             AS to_id,
  'admin'        AS operator,
  DATE_ADD('2025-09-01', INTERVAL r.id SECOND) AS migrate_time
FROM crm_record r
WHERE r.related_type = 'lead'
  AND r.related_id IN (1111, 1112)
  AND NOT EXISTS (
    SELECT 1 FROM crm_record_migration_log m WHERE m.record_id = r.id
  )
LIMIT 20;

